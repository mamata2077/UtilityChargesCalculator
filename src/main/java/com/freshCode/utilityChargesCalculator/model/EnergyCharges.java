package com.freshCode.utilityChargesCalculator.model;

public class EnergyCharges {
    double firstSlabCharges;
    double secondSlabCharges;
    double thirdSlabCharges;
    double lastSlabCharges;
    double totalEnergyCharges;

    public double getFirstSlabCharges() {
        return firstSlabCharges;
    }

    public void setFirstSlabCharges(double firstSlabCharges) {
        this.firstSlabCharges = firstSlabCharges;
    }

    public double getSecondSlabCharges() {
        return secondSlabCharges;
    }

    public void setSecondSlabCharges(double secondSlabCharges) {
        this.secondSlabCharges = secondSlabCharges;
    }

    public double getThirdSlabCharges() {
        return thirdSlabCharges;
    }

    public void setThirdSlabCharges(double thirdSlabCharges) {
        this.thirdSlabCharges = thirdSlabCharges;
    }

    public double getLastSlabCharges() {
        return lastSlabCharges;
    }

    public void setLastSlabCharges(double lastSlabCharges) {
        this.lastSlabCharges = lastSlabCharges;
    }

    public double getTotalEnergyCharges() {
        return totalEnergyCharges;
    }

    public void setTotalEnergyCharges(double totalEnergyCharges) {
        this.totalEnergyCharges = totalEnergyCharges;
    }
    
}
